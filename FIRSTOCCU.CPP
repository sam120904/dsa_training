#include<iostream>
#include<vector>
using namespace std;
binarysearch(int arr[], int n, int key){
    int low = 0;
    int high = n-1;

    while(low <= high){
        int mid = low + (high - low)/2;

        if(arr[mid] == key){
            return mid;
        }
        else if(arr[mid] < key){
            low = mid + 1;
        }
        else{
            high = mid - 1;
        }
    }
    return -1;
}
int main(){
    int arr[] = {1,2,3,4,4,4,4,5,6};
    int n = sizeof(arr)/sizeof(arr[0]);
    int key = 4;

    cout << binarysearch(arr,n,key);
}